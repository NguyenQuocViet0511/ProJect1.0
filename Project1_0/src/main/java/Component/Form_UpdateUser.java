    public void LoadDataUser() {

        txthovaten.setText(Const.user_update.getHovaTen());
        txtemail.setText("" + Const.user_update.getEmail());
        txttendangnhap.setText(Const.user_update.getUserName());
        txtdiachi.setText(Const.user_update.getDiaChi());
        if (Const.user_update.getGioitinh().equals("Nam")) {
            RdnNam.setSelected(true);
        }
        if (Const.user_update.getGioitinh().equals("Nữ")) {
            RdnNu.setSelected(true);
        }
        String NgaySinh = Const.user_update.getNgaySinh().toString();
        String[] Tachchuoi = NgaySinh.split("-");
        CbnNam.setSelectedItem(Tachchuoi[0]);
        cbnThang.setSelectedItem(Tachchuoi[1]);
        cbnNgay.setSelectedItem(Tachchuoi[2]);
        String a = "Bình Thường";
        String b = "Khóa";
        if (Const.user_update.getTrangThai().equals(a)) {
            RdnBinhThuong.setSelected(true);
            RdnKhoa.setSelected(false);
        } else if (Const.user_update.getTrangThai().equals(b)) {
            RdnKhoa.setSelected(true);
            RdnBinhThuong.setSelected(false);
        }

    }

