    private void btndongyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndongyActionPerformed
        int option = JOptionPane.showConfirmDialog(null, "Bạn Có Muốn sửa Thông tin Này Không", "Nhắc Nhở", JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE, new ImageIcon(getClass().getResource("/img/icons8_ask_question_20px_3.png")));

        if (option == 0) {
            User Update = Const.session.get(User.class, Const.user_update.getUserName());
            Update.setUserName(txttendangnhap.getText());
            Update.setHovaTen(txthovaten.getText());
            Update.setEmail(txtemail.getText());
            if (RdnNam.isSelected()) {
                Update.setGioitinh("Nam");
            } else {
                Update.setGioitinh("Nữ");
            }
            String NgaySinh = CbnNam.getSelectedItem() + "-" + cbnThang.getSelectedItem() + "-" + cbnNgay.getSelectedItem();
            Update.setNgaySinh(Date.valueOf(NgaySinh));
            Update.setDiaChi(txtdiachi.getText());
            if (RdnBinhThuong.isSelected()) {
                Update.setTrangThai("Bình Thường");
            } else {
                Update.setTrangThai("Khóa");
            }

            Const.session.getTransaction().begin();
            Const.session.delete(Update);
            Const.session.save(Update);
            Const.session.getTransaction().commit();
            JOptionPane.showMessageDialog(null, "sửa Thành Công", "Nhắc Nhở", JOptionPane.INFORMATION_MESSAGE, new ImageIcon(getClass().getResource("/img/icons8_ask_question_20px_3.png")));
            Const.Load = false;
            this.dispose();

        } else {
            JOptionPane.showMessageDialog(null, "sửa Thất bại", "Nhắc Nhở", JOptionPane.INFORMATION_MESSAGE, new ImageIcon(getClass().getResource("/img/icons8_ask_question_20px_3.png")));

        }
    }//GEN-LAST:event_btndongyActionPerformed

    private void btnkhoiphucmatkhauActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnkhoiphucmatkhauActionPerformed
        int option = JOptionPane.showConfirmDialog(null, "Bạn Có Muốn Reset Mật Khẩu Không", "Nhắc Nhở", JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE, new ImageIcon(getClass().getResource("/img/icons8_ask_question_20px_3.png")));
        if (option == 0) {
            String Search = Const.user_update.getUserName();
            User Update = Const.session.get(User.class, Search);
            Update.setPassword(txttendangnhap.getText());
            Const.session.getTransaction().begin();
            Const.session.save(Update);
            Const.session.getTransaction().commit();
            JOptionPane.showMessageDialog(null, "Thay Đổi Thành Công", "Nhắc Nhở", JOptionPane.INFORMATION_MESSAGE, new ImageIcon(getClass().getResource("/img/icons8_ask_question_20px_3.png")));

            Const.Load = false;

            this.dispose();
        } else if (option == 1) {
            JOptionPane.showMessageDialog(null, "Thay Đổi Thất bại", "Nhắc Nhở", JOptionPane.INFORMATION_MESSAGE, new ImageIcon(getClass().getResource("/img/icons8_ask_question_20px_3.png")));

        } else {

        }
    }//GEN-LAST:event_btnkhoiphucmatkhauActionPerformed
    private void AddDate() {
        for (int i = 1; i <= 9; i++) {
            cbnNgay.addItem(0 + "" + i);

        }
        for (int i = 10; i <= 31; i++) {
            cbnNgay.addItem("" + i);

        }
        for (int i = 1; i <= 9; i++) {
            cbnThang.addItem(0 + "" + i);

        }
        for (int i = 10; i <= 12; i++) {
            cbnThang.addItem("" + i);

        }
        for (int i = 1991; i <= 2200; i++) {
            CbnNam.addItem("" + i);

        }
    }
