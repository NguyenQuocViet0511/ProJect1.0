    private void BtnCapNhatThongTinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCapNhatThongTinActionPerformed
        if (Edit) {
            BtnCapNhatThongTin.setIcon(new javax.swing.ImageIcon(getClass().getResource("/img/icons8_checkmark_20px_1.png")));
            TxtEmail.setEnabled(true);
            TxtDiaChi.setEnabled(true);
            TxtHovaTen.setEnabled(true);
            RdnNam.setEnabled(true);
            RdnNu.setEnabled(true);
            CbnNam.setEnabled(true);
            CbnNgay.setEnabled(true);
            CbnThang.setEnabled(true);
            Edit = false;
            BtnCapNhatThongTin.setText("Lưu Thông Tin");
        } else {
            int option = JOptionPane.showConfirmDialog(null, "Bạn Có Muốn Cập Nhật Lại Thông Tin Này Không", "Nhắc Nhở", JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE, new ImageIcon(getClass().getResource("/img/icons8_warning_shield_20px_1.png")));

            if (option == 0) {
                BtnCapNhatThongTin.setIcon(new javax.swing.ImageIcon(getClass().getResource("/img/suado_20.png")));
                TxtEmail.setEnabled(false);
                TxtDiaChi.setEnabled(false);
                TxtHovaTen.setEnabled(false);
                RdnNam.setEnabled(false);
                RdnNu.setEnabled(false);
                CbnNam.setEnabled(false);
                CbnNgay.setEnabled(false);
                CbnThang.setEnabled(false);
                BtnCapNhatThongTin.setText("Cập Nhật Thông Tin");
                User Sua = Const.session.get(User.class, Const.user.getUserName());
                Sua.setHovaTen(TxtHovaTen.getText());
                Sua.setEmail(TxtEmail.getText());
                String NgaySinh = CbnNam.getSelectedItem() + "-" + CbnThang.getSelectedItem() + "-" + CbnNgay.getSelectedItem();
                Sua.setNgaySinh(java.sql.Date.valueOf(NgaySinh));
                if (RdnNam.isSelected()) {
                    Sua.setGioitinh("Nam");
                } else {
                    Sua.setGioitinh("Nữ");
                }

                Sua.setDiaChi(TxtDiaChi.getText());
                Const.session.getTransaction().begin();
                Const.session.save(Sua);
                Const.session.getTransaction().commit();
                JOptionPane.showMessageDialog(null, "Cập Nhật Thành Công", "Nhắc Nhở", JOptionPane.INFORMATION_MESSAGE, new ImageIcon(getClass().getResource("/img/icons8_ask_question_20px_3.png")));
                Edit = true;
            } else {
                LoadDataLen();
                BtnCapNhatThongTin.setIcon(new javax.swing.ImageIcon(getClass().getResource("/img/suado_20.png")));
                TxtEmail.setEnabled(false);
                TxtDiaChi.setEnabled(false);
                TxtHovaTen.setEnabled(false);
                RdnNam.setEnabled(false);
                RdnNu.setEnabled(false);
                CbnNam.setEnabled(false);
                CbnNgay.setEnabled(false);
                CbnThang.setEnabled(false);
                BtnCapNhatThongTin.setText("Cập Nhật Thông Tin");
                Edit = true;
                JOptionPane.showMessageDialog(null, "Cập Nhật Thất Bại", "Nhắc Nhở", JOptionPane.INFORMATION_MESSAGE, new ImageIcon(getClass().getResource("/img/icons8_ask_question_20px_3.png")));
            }

        }
    }//GEN-LAST:event_BtnCapNhatThongTinActionPerformed

