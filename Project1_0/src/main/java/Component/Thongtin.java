    @Override
    protected void paintComponent(Graphics grphcs) {
        Graphics2D g2 = (Graphics2D) grphcs;
        g2.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);
        GradientPaint gra = new GradientPaint(0, 0, Color.white, getWidth(), 0, new Color(93, 58, 196));
        g2.setPaint(gra);
        g2.fillRect(0, 0, getWidth(), getHeight());
        super.paintComponent(grphcs);
    }

    public void LoadDataLen() {
        TxtHovaTen.setText(Const.user.getHovaTen());
        TxtEmail.setText(Const.user.getEmail());
        TxtDiaChi.setText(Const.user.getDiaChi());
        if (Const.user.getGioitinh().endsWith("Nam")) {
            RdnNam.setSelected(true);
        } else {
            RdnNu.setSelected(true);
        }
        String NgaySinh = Const.user.getNgaySinh().toString();
        String[] Tachchuoi = NgaySinh.split("-");

        CbnNam.setSelectedItem(Tachchuoi[0]);
        CbnThang.setSelectedItem(Tachchuoi[1]);
        CbnNgay.setSelectedItem(Tachchuoi[2]);

    }

    private void AddDate() {
        for (int i = 1; i <= 9; i++) {
            CbnNgay.addItem(0 + "" + i);

        }
        for (int i = 10; i <= 31; i++) {
            CbnNgay.addItem("" + i);

        }
        for (int i = 1; i <= 9; i++) {
            CbnThang.addItem(0 + "" + i);

        }
        for (int i = 10; i <= 12; i++) {
            CbnThang.addItem("" + i);

        }
        for (int i = 1991; i <= 2200; i++) {
            CbnNam.addItem("" + i);

        }
    }
